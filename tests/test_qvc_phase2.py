class TestSRTPKeyIntegration:
    """WP #651: SRTP key integration from QKD.

    The AV pipeline uses encryption keys from the key generator for
    encrypting media streams. These tests verify the key flows from
    generator -> AV encryption.
    """

    def test_key_rotation_wire_format(self):
        """Wire format is 4-byte key index + encrypted payload."""
        enc = AESEncryption()
        key = os.urandom(16)
        key_idx = 7
        plaintext = b"audio frame data"
        ct = enc.encrypt(plaintext, key)
        wire = key_idx.to_bytes(4, "big") + ct
        # Parse
        parsed_idx = int.from_bytes(wire[:4], "big")
        parsed_ct = wire[4:]
        assert parsed_idx == 7
        assert enc.decrypt(parsed_ct, key) == plaintext

    def test_key_generator_produces_aes_compatible_key(self):
        """RandomKeyGenerator(128) should produce 16-byte key usable by AES."""
        gen = RandomKeyGenerator(key_length=128)
        gen.generate_key()
        key = gen.get_key()
        assert len(key) == 16
        enc = AESEncryption()
        ct = enc.encrypt(b"stream payload!!", key)
        assert enc.decrypt(ct, key) == b"stream payload!!"

    def test_encrypt_scheme_registry(self):
        """AES, XOR, DEBUG are all registered in the factory."""
        aes = create_encrypt_scheme("AES")
