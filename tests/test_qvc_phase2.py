class TestFileKeyGeneratorResourceManagement:
    """WP #652: FileKeyGenerator resource management."""

    def test_context_manager_closes_file(self):
        """FileKeyGenerator should close the file handle on __exit__."""
        with tempfile.NamedTemporaryFile(delete=False) as f:
            f.write(os.urandom(256))
            tmpname = f.name
        try:
            with FileKeyGenerator(file_name=tmpname, key_length=128) as gen:
                gen.generate_key()
                key = gen.get_key()
                assert len(key) == 16
            # After exiting, the file should be closed
            assert gen._file is None
        finally:
            Path(tmpname).unlink()

    def test_explicit_close(self):
        with tempfile.NamedTemporaryFile(delete=False) as f:
            f.write(os.urandom(256))
            tmpname = f.name
        try:
            gen = FileKeyGenerator(file_name=tmpname, key_length=128)
            gen.generate_key()
            assert gen._file is not None
            gen.close()
            assert gen._file is None
        finally:
            Path(tmpname).unlink()

    def test_double_close_is_safe(self):
        with tempfile.NamedTemporaryFile(delete=False) as f:
            f.write(os.urandom(256))
            tmpname = f.name
        try:
            gen = FileKeyGenerator(file_name=tmpname, key_length=128)
            gen.generate_key()
            gen.close()
            gen.close()  # Should not raise
            assert gen._file is None
        finally:
            Path(tmpname).unlink()

    def test_missing_file_raises(self):
        gen = FileKeyGenerator(file_name="/tmp/nonexistent_qvc_key_file.bin",
                               key_length=128)
        with pytest.raises(FileNotFoundError):
            gen.generate_key()

    def test_zero_key_length_raises(self):
        gen = FileKeyGenerator(key_length=0)
        with pytest.raises(ValueError, match="nonzero"):
            gen.generate_key()

    def test_del_closes_file(self):
        """__del__ should close the file handle to prevent leaks."""
        with tempfile.NamedTemporaryFile(delete=False) as f:
            f.write(os.urandom(256))
            tmpname = f.name
        try:
            gen = FileKeyGenerator(file_name=tmpname, key_length=128)
            gen.generate_key()
            file_obj = gen._file
            assert not file_obj.closed
            gen.__del__()
            assert file_obj.closed
        finally:
            Path(tmpname).unlink()


